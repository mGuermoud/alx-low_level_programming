#include "3-calc.h"

/**
 * op_add - return the sum of two int
 * @a: first param
 * @b: second param
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the difference of two int
 * @a: first parameter
 * @b: second parameter
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the product of two int
 * @a: first parameter
 * @b: second parameter
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - retunr the division of two int
 * @a: first parameter
 * @b: second parameter
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the remainder of two int
 * @a: first parameter
 * @b: second parameter
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
